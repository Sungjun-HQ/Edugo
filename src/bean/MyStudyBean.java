package bean;

public class MyStudyBean {
	private int video_idx;
	private String video_name;
	private String video_filename;
	private String video_date;
	private String teacher_name;
	private String sub_name;
	private int sub_idx;

	public int getVideo_idx() {
		return video_idx;
	}

	public String getVideo_name() {
		return video_name;
	}

	public String getVideo_filename() {
		return video_filename;
	}

	public String getVideo_date() {
		return video_date;
	}

	public String getTeacher_name() {
		return teacher_name;
	}

	public String getSub_name() {
		return sub_name;
	}

	public int getSub_idx() {
		return sub_idx;
	}

	public void setVideo_idx(int video_idx) {
		this.video_idx = video_idx;
	}

	public void setVideo_name(String video_name) {
		this.video_name = video_name;
	}

	public void setVideo_filename(String video_filename) {
		this.video_filename = video_filename;
	}

	public void setVideo_date(String video_date) {
		this.video_date = video_date;
	}

	public void setTeacher_name(String teacher_name) {
		this.teacher_name = teacher_name;
	}

	public void setSub_name(String sub_name) {
		this.sub_name = sub_name;
	}

	public void setSub_idx(int sub_idx) {
		this.sub_idx = sub_idx;
	}

}
